# 자동차 정보
# 대여기록 정보
# 할인 정책

# 자동차 - 세단 or SUV
# 2022/11/1 ~ 2022/11/30
# 30 50..<200
    
SELECT
    A.CAR_ID,
    A.CAR_TYPE,
    ROUND(30 * A.DAILY_FEE * (100 - C.DISCOUNT_RATE) / 100, 0) AS FEE
FROM CAR_RENTAL_COMPANY_CAR A
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN C ON A.CAR_TYPE = C.CAR_TYPE
WHERE A.CAR_TYPE IN ('세단', 'SUV')
  AND C.DURATION_TYPE = '30일 이상'
  AND A.CAR_ID NOT IN (
      SELECT B.CAR_ID
      FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY B
      WHERE B.START_DATE <= '2022-11-30'
        AND B.END_DATE >= '2022-11-01'
  )
  AND ROUND(30 * A.DAILY_FEE * (100 - C.DISCOUNT_RATE) / 100, 0) >= 500000
  AND ROUND(30 * A.DAILY_FEE * (100 - C.DISCOUNT_RATE) / 100, 0) < 2000000
ORDER BY FEE DESC, A.CAR_TYPE ASC, A.CAR_ID DESC;